module mul (
    input  logic [15:0] x, y,
    output logic [15:0] Pm,
    output logic [3:0]  flags
);

    logic [21:0] product; 
    logic [4:0] P_e, newP_e, Xe, Ye;
    logic [9:0] fraction;
    logic X_nonzero, Y_nonzero, X_subnorm, Y_subnorm;
    logic [10:0] X_m, Y_m;

    always_comb begin
        // multiply mantissas with leading one
        
        Xe = x[14:10];
        Ye = y[14:10];

        X_nonzero = |x[14:0];
        Y_nonzero = |y[14:0];

        X_subnorm = (Xe == 0) & (X_nonzero);
        Y_subnorm = (Ye == 0) & (Y_nonzero);

        X_m = {X_nonzero & (~X_subnorm), x[9:0]};
        Y_m = {Y_nonzero & (~Y_subnorm), y[9:0]};
        //X_m = (Xe == 0) ? {1'b0, x[9:0]} : {1'b1, x[9:0]};
        //Y_m = (Ye == 0) ? {1'b0, y[9:0]} : {1'b1, y[9:0]};

        product = X_m * Y_m;

        //P_e = x[14:10] + y[14:10] - 5'd15;
        P_e = Xe + Ye -15;
        //$display("Product: %b", product);
        if (product[21]) begin
            fraction = product[20:11];  // shift if P_e increases
            newP_e = P_e + 5'd1;
        end 
        else begin
            fraction = product[19:10];  // if same P_e
            newP_e = P_e;
        end

        // combine all parts into IEEE single precision number
        Pm = {x[15] ^ y[15], newP_e, fraction};

        // add flags
        flags = 4'b0000; // Reset flags
       
    end

endmodule

